def _fallback_short_name(text: str) -> str:
    raw = re.sub(r"\([^)]*\)", "", text or "")
    raw = re.sub(r"[\"'`]+", "", raw)
    raw = re.sub(r"\s+", " ", raw).strip(" ,.;:-")
    if not raw:
        return "Критерий"
    words = raw.split()
    if len(words) <= 3:
        return raw
    return " ".join(words[:3])


def derive_criterion_metadata(description: str) -> tuple[str, str]:
    text = (description or "").strip()
    low = text.lower()
    for pattern, cid, short_name in _CRITERION_RULES:
        if re.search(pattern, low):
            return cid, short_name
    short_name = _fallback_short_name(text)
    cid = _transliterate(short_name)
    cid = re.sub(r"[^a-z0-9]+", "_", cid)
    cid = re.sub(r"_+", "_", cid).strip("_")
    return cid or "criterion", short_name


def build_seed_criteria(criteria_texts: list[str]) -> list[dict[str, Any]]:
    seen: dict[str, int] = {}
    out: list[dict[str, Any]] = []
    for description in criteria_texts:
        cid, short_name = derive_criterion_metadata(description)
        index = seen.get(cid, 0) + 1
        seen[cid] = index
        final_id = cid if index == 1 else f"{cid}_{index}"
        out.append(
            {
                "id": final_id,
                "name": short_name,
                "description": description.strip(),
                "weight": 1,
            }
        )
    return out
